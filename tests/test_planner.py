class TestPlanDraft:
    def test_fields(self):
        draft = PlanDraft(
            steps=[("Step 1", "Detail 1"), ("Step 2", None)],
            source="model",
            notes="Generated by backend.",
        )
        assert len(draft.steps) == 2
        assert draft.source == "model"
        assert draft.notes == "Generated by backend."

    def test_steps_with_none_detail(self):
        draft = PlanDraft(steps=[("Step", None)], source="fallback", notes="")
        title, detail = draft.steps[0]
        assert title == "Step"
        assert detail is None


class TestBuildPlanDraft:
    def test_backend_failure_reason_recorded_in_fallback_notes(self, tmp_path: Path):
        class BrokenApp:
            def run(self, request):
                raise RuntimeError("planner backend unavailable")

        draft = build_plan_draft(
            BrokenApp(),
            goal="do work",
            backend_name="broken",
            strategy_name="direct",
            working_directory=tmp_path,
            domain=None,
        )
        assert draft.source == "fallback"
        assert "planner backend unavailable" in draft.notes
