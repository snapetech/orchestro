class TestParseNumberedSteps:
    def test_period_delimiter(self):
        text = "1. First step\n2. Second step\n3. Third step"
        steps = parse_numbered_steps(text)
        assert steps == ["First step", "Second step", "Third step"]

    def test_parenthesis_delimiter(self):
        text = "1) Do this\n2) Do that"
        steps = parse_numbered_steps(text)
        assert steps == ["Do this", "Do that"]

    def test_colon_delimiter(self):
        text = "1: Step one\n2: Step two"
        steps = parse_numbered_steps(text)
        assert steps == ["Step one", "Step two"]

    def test_dash_delimiter(self):
        text = "- First\n- Second\n- Third"
        steps = parse_numbered_steps(text)
        assert steps == ["First", "Second", "Third"]

    def test_mixed_delimiters(self):
        text = "1. First\n- Second\n3) Third"
        steps = parse_numbered_steps(text)
        assert len(steps) == 3

    def test_empty_string_returns_empty(self):
        assert parse_numbered_steps("") == []

    def test_blank_lines_skipped(self):
        text = "1. First\n\n2. Second\n\n3. Third"
        steps = parse_numbered_steps(text)
        assert len(steps) == 3

    def test_leading_trailing_whitespace_stripped(self):
        text = "1.   Padded step   "
        steps = parse_numbered_steps(text)
        assert steps == ["Padded step"]

    def test_non_list_lines_ignored(self):
        text = "Here is my plan:\n1. Do this\nSome prose.\n2. Do that"
        steps = parse_numbered_steps(text)
        assert steps == ["Do this", "Do that"]

    def test_empty_step_filtered_out(self):
        text = "1. \n2. Real step"
        steps = parse_numbered_steps(text)
        assert "Real step" in steps
        assert "" not in steps

    def test_semicolon_delimiter(self):
        text = "1:- Step one\n2:- Step two"
        # The regex is "1[\).\:-]" so :- won't match but : does match
        steps = parse_numbered_steps(text)
        # At minimum shouldn't crash
        assert isinstance(steps, list)


