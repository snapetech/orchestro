class TestFallbackPlanDraft:
    def test_returns_plan_draft(self):
        draft = fallback_plan_draft(goal="do something", domain=None)
        assert isinstance(draft, PlanDraft)

    def test_has_five_steps(self):
        draft = fallback_plan_draft(goal="do something", domain=None)
        assert len(draft.steps) == 5

    def test_steps_are_tuples(self):
        draft = fallback_plan_draft(goal="do something", domain=None)
        for step in draft.steps:
            assert isinstance(step, tuple)
            assert len(step) == 2

    def test_goal_appears_in_steps(self):
        draft = fallback_plan_draft(goal="build the widget", domain=None)
        combined = " ".join(str(s) for s in draft.steps)
        assert "build the widget" in combined

    def test_domain_hint_in_first_step(self):
        draft = fallback_plan_draft(goal="do task", domain="coding")
        first_detail = draft.steps[0][1]
        assert "coding" in first_detail

    def test_no_domain_no_domain_hint(self):
        draft = fallback_plan_draft(goal="do task", domain=None)
        first_detail = draft.steps[0][1]
        # Should not have stray "None" or "Domain focus: None" text
        assert "None" not in first_detail

    def test_source_is_fallback(self):
        draft = fallback_plan_draft(goal="task", domain=None)
        assert draft.source == "fallback"

    def test_notes_not_empty(self):
        draft = fallback_plan_draft(goal="task", domain=None)
        assert draft.notes

    def test_notes_include_reason_when_provided(self):
        draft = fallback_plan_draft(goal="task", domain=None, reason="backend plan generation failed: boom")
        assert "boom" in draft.notes

    def test_step_titles_not_empty(self):
        draft = fallback_plan_draft(goal="task", domain="devops")
        for title, _ in draft.steps:
            assert title.strip()


